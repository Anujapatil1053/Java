public class ArithmaticOperator
{
	public static void main(String args[])
	{
		int No1 = 24, No2 = 27, Add,Sub,Mul,Div,Mod;
		
		Add = No1 + No2;
		Sub = No1 - No2;
		Mul = No1 * No2;
		Div = No1 / No2;
		Mod = No1 % No2;

		System.out.println("\t Addition Of " + No1 + " + " + No2 +  " = " +Add);
		System.out.println("\t Substraction Of " + No1 + " - " + No2 +  " = " +Sub);
		System.out.println("\t Multiplication Of " + No1 + " * " + No2 + " = " +Mul);
		System.out.println("\t Division Of " + No1 + " / " + No2 + " = " +Div);
		System.out.println("\t Modulous Of " + No1 + " % " + No2 +  " = " +Mod);
	
	}



}
