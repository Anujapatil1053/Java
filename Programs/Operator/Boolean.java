public class Boolean
{
	public static void main(String args[])
	{
		int No1 = 24, No2 = 27;

		boolean Bool;

		Bool = No1 > No2;
		System.out.println(+ No1 + " > " + No2 + " = " + Bool);

		Bool = No1 < No2;
		System.out.println(+ No1 + " < " + No2 + " = " + Bool);
		
		Bool = No1 <= No2;
		System.out.println(+ No1 + " <= " + No2 + " = " + Bool);
		
		Bool = No1 >= No2;
		System.out.println(+ No1 + " >= " + No2 + " = " + Bool);
		
		Bool = No1 != No2;
		System.out.println(+ No1 + " != " + No2 + " = " + Bool);
		
	}
}