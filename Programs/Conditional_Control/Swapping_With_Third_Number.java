public class Swapping_With_Third_Number
{
	public static void main(String args[])
	{
		int No1 = 10, No2 = 20,Temp = 0;

		System.out.println("\n Before Swapping No1 = " + No1 + ", No2 = "+ No2);

		Temp = No1; 
		No1 = No2;
		No2 = Temp;
		
		System.out.println("\n After Swapping No1 = " + No1 + ", No2 = "+ No2);
	}
}



