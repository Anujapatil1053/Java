public class Prime
{
	public static void main(String args[])
	{
		int No = 29, Cnt = 0;                  
		
		for(int i = 2; Cnt / 2; i++)
		{
			if(No % Cnt == 0)
			{
				System.out.println("It Is Prime Number");
			}
			else
			{	
				System.out.println("It Is Prime Number");
			}
		}	
	}
}
